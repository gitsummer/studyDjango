package com.taxi.take;

public class Point {
	
	
private int x;
private int y;

public Point()
{}
public Point(int xx,int yy)
{
	this.x=xx;
	this.y=yy;


}

public int getX() {
	return x;
}
public void setX(int x) {
	this.x = x;
}
public int getY() {
	return y;
}
public void setY(int y) {
	this.y = y;
}



}
